int _pinyin_to_hanzi_bun(unsigned short *_pinyin_key_buffer, unsigned short *hanzi)
{
	int i = 0, j = 0;
	char input_string[10] ={0};
	int cnt = 0;

	usTochar(input_string, _pinyin_key_buffer);

	//===============================================================================
	if (strcmp(input_string, "long") == 0)
	{
		strcat(input_string, "1");
	}
	//===============================================================================
	for (i = 0; *pinyin_hanzi_table[i].unicode != 0x0; i++) 
	{
		if (strcmp(input_string, pinyin_hanzi_table[i].pinyin) == 0) 
		{
			uscpy(hanzi, pinyin_hanzi_table[i].unicode);

			return j;
		}
	}

	*hanzi = NULL;

	return 0;	
}

