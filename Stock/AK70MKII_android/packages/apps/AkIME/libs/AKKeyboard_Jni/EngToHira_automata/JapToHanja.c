static int com_wcscmp(const wchar* cs,const wchar * ct)
{
	while (*cs == *ct)
	{
		if (*cs == 0)
			return 0;
		cs++;
		ct++;
	}
	return *cs - *ct;
}

static wchar* com_wcscpy (wchar *dest, const wchar *src) 
{
	int i;

	for( i=0;src[i];i++) 
	{
		dest[i]=src[i]; 
	}
	dest[i]=0; 
	return dest;
}


static wchar* com_wcscat(wchar * dest,const wchar * src)
{

	wchar *d = dest;

	for (; *dest !=0; dest++);

	while (*src != 0)
	{
		*dest = *src;
		dest++;
		src++;
	}

	*dest = 0;

	return d;
}

